package com.company;

public class FoodData {
    private String name;
    private Double caloriesFor100Grams;
    private Double proteinFor100Grams;
    private Double carbohydratesFor100Grams;
    private Double fatsFor100Grams;

    public Double getFatsFor100Grams() {
        return fatsFor100Grams;
    }

    public void setFatsFor100Grams(Double fatsFor100Grams) {
        this.fatsFor100Grams = fatsFor100Grams;
    }


    public Double getCarbohydratesFor100Grams() {
        return carbohydratesFor100Grams;
    }

    public void setCarbohydratesFor100Grams(Double carbohydratesFor100Grams) {
        this.carbohydratesFor100Grams = carbohydratesFor100Grams;
    }

    public Double getProteinFor100Grams() {
        return proteinFor100Grams;
    }

    public void setProteinFor100Grams(Double proteinFor100Grams) {
        this.proteinFor100Grams = proteinFor100Grams;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getCaloriesFor100Grams() {
        return caloriesFor100Grams;
    }

    public void setCaloriesFor100Grams(Double caloriesFor100Grams) {
        this.caloriesFor100Grams = caloriesFor100Grams;
    }

}
